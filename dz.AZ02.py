import pandas as pd

# Создание данных
data = {
    'Студент': ['Иван', 'Анна', 'Мария', 'Сергей', 'Виктория', 'Дмитрий', 'Ольга', 'Николай', 'Елена', 'Алексей'],
    'Математика': [5, 4, 5, 3, 4, 5, 2, 4, 5, 4],
    'Физика': [3, 4, 5, 3, 3, 4, 2, 5, 4, 4],
    'Химия': [2, 5, 4, 4, 5, 3, 5, 4, 4, 5],
    'Литература': [4, 3, 5, 4, 4, 5, 3, 5, 5, 4],
    'История': [5, 4, 3, 5, 4, 5, 4, 3, 4, 5]
}

# Создание DataFrame
df = pd.DataFrame(data)

# Вывод первых несколько строк DataFrame
print(df.head())

# Средняя оценка по каждому предмету
mean_scores = df.mean(numeric_only=True)

# Медианная оценка по каждому предмету
median_scores = df.median(numeric_only=True)

# Квантили Q1 и Q3 для оценок по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
IQR_math = Q3_math - Q1_math

# Стандартное отклонение по каждому предмету
std_deviation = df.std(numeric_only=True)

# Вывод результатов
print("Средние оценки по предметам:\n", mean_scores)
print("\nМедианные оценки по предметам:\n", median_scores)
print("\nКвантиль Q1 по математике:", Q1_math)
print("Квантиль Q3 по математике:", Q3_math)
print("Межквартильный размах по математике:", IQR_math)
print("\nСтандартное отклонение по предметам:\n", std_deviation)

